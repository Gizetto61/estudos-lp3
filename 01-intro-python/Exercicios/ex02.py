# Escreva um código que solicita ao usuário três numeros e apresenta a média aritimética

print('MÉDIA ARITIMÉTICA!!!')
num1 = int(input('Digite o 1° valor: '))
num2 = int(input('Digite o 2° valor: '))
num3 = int(input('Digite o 3° valor: '))

media = (num1 + num2 + num3) / 3

print('A média dos três numeros é ', media)